
package com.magicbus.careercatalyst;

public class StudentProfileRequest {
    private String currentSkills;
    private String interests;
    private String educationLevel;
    private String preferredLanguage;
    private String userMessage;
    private String conversationContext;
    private String studentName;
    private Boolean roadmapRequested;
    private String aiDataInterest;
    private String deviceAccess;
    private Integer timePerWeekHours;
    private Integer mathComfort;
    private Integer problemSolvingConfidence;
    private Integer englishComfort;

    // Getters and Setters
    public String getCurrentSkills() {
        return currentSkills;
    }

    public void setCurrentSkills(String currentSkills) {
        this.currentSkills = currentSkills;
    }

    public String getInterests() {
        return interests;
    }

    public void setInterests(String interests) {
        this.interests = interests;
    }

    public String getEducationLevel() {
        return educationLevel;
    }

    public void setEducationLevel(String educationLevel) {
        this.educationLevel = educationLevel;
    }

    public String getPreferredLanguage() {
        return preferredLanguage;
    }

    public void setPreferredLanguage(String preferredLanguage) {
        this.preferredLanguage = preferredLanguage;
    }

    public String getUserMessage() {
        return userMessage;
    }

    public void setUserMessage(String userMessage) {
        this.userMessage = userMessage;
    }

    public String getConversationContext() {
        return conversationContext;
    }

    public void setConversationContext(String conversationContext) {
        this.conversationContext = conversationContext;
    }

    public String getStudentName() {
        return studentName;
    }

    public void setStudentName(String studentName) {
        this.studentName = studentName;
    }

    public Boolean getRoadmapRequested() {
        return roadmapRequested;
    }

    public void setRoadmapRequested(Boolean roadmapRequested) {
        this.roadmapRequested = roadmapRequested;
    }

    public String getAiDataInterest() {
        return aiDataInterest;
    }

    public void setAiDataInterest(String aiDataInterest) {
        this.aiDataInterest = aiDataInterest;
    }

    public String getDeviceAccess() {
        return deviceAccess;
    }

    public void setDeviceAccess(String deviceAccess) {
        this.deviceAccess = deviceAccess;
    }

    public Integer getTimePerWeekHours() {
        return timePerWeekHours;
    }

    public void setTimePerWeekHours(Integer timePerWeekHours) {
        this.timePerWeekHours = timePerWeekHours;
    }

    public Integer getMathComfort() {
        return mathComfort;
    }

    public void setMathComfort(Integer mathComfort) {
        this.mathComfort = mathComfort;
    }

    public Integer getProblemSolvingConfidence() {
        return problemSolvingConfidence;
    }

    public void setProblemSolvingConfidence(Integer problemSolvingConfidence) {
        this.problemSolvingConfidence = problemSolvingConfidence;
    }

    public Integer getEnglishComfort() {
        return englishComfort;
    }

    public void setEnglishComfort(Integer englishComfort) {
        this.englishComfort = englishComfort;
    }
}
